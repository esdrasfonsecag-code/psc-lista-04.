public class Exercicio06 {
    public static void main(String[] args) {
        // Números um abaixo do outro
        System.out.println("Números um por linha:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        // Números lado a lado
        System.out.println("\nNúmeros na mesma linha:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}
